package com.joinventure.entities.DTOs;

public class TechnologyDTO {

	private String user_name;
	private String last_name;
	private String email;
	private String name_framework;
	private String name_language;

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getName_framework() {
		return name_framework;
	}

	public void setName_framework(String name_framework) {
		this.name_framework = name_framework;
	}

	public String getName_language() {
		return name_language;
	}

	public void setName_language(String name_language) {
		this.name_language = name_language;
	}

}
